/**
 * _isupper - checks if a number is in upper or lower case
 *@c - character to be checked
 *Description: Prints the alphabet with _putchae
 * Return: 1 if c is uppercase or 0 if otherwise
 */

int _isupper(int c)
{
if (c >= 65 && c <= 90)
{
return (1);
}
else
{
return (0);
}
}
